import math

# 1. Ask the user to enter a number "x"
x_value = float(input("X Value: "))

# 2. Ask the user to enter a number "y"
y_value = float(input("Y Value: "))

# 3. Prints out the number "x" raised to the power "y"
print(str(x_value**y_value))

# 4. Prints out the log (base 2) of "x"

print(str(math.log(x_value,2)))